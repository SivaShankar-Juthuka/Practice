class Solution {
  public:
    int majorityElement(vector<int>& arr) {
        // code here
        int n = arr.size()/2;
        unordered_map<int, int> mp;
        for(auto it: arr) {
            mp[it] ++;
        }
        
        int res = -1;
        for(auto it: mp) {
            if(it.second > n) {
                res = it.first;
            }
        }
        
        return res;
    }
};


